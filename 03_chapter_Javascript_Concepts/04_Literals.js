// Literals are values which represents actual data
// Fixed value written in code

// Number literals - 1,2,3 etc.,
// String literals - dhoni, ashwin, jagadesh etc.,
// Boolean literals - true, false
// Null literals - null
// Undefined literals - undefined

var myName = "Jagadesh"; //string literal
var myAge = 26; // number literal
var isGenderMale = true ; // boolean literal
var purchase = null;
var rupee = undefined;

console.log(typeof(myName));
console.log(typeof(myAge));
console.log(typeof(isGenderMale));
console.log(typeof(purchase)); // Note: Data type of Null is object
console.log(typeof(rupee)); // Note: Data type of undefined is undefined
