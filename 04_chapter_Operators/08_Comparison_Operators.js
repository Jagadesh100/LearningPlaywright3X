// Comparison Operator compares two values and return boolean value (true/false)

// >,<.>=,<=

/**
 * = assignment opearot
 * == loosely compare - checks only value
 * === strictly compare = checks value and data type
 */

/**
 * != - not equal to - only value check
 * !== - strictly not equal to - both value and data type
 */
console.log(7 == "7"); // true  //loose comparison , checks only the value
console.log(7 === "7"); // false // strict comparison, check value and data type

console.log(5 != "5"); // false
console.log(5 !== "5"); // true

